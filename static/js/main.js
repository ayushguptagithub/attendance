    function validate(){
        var password = document.getElementById('password').value;
        var passwordError = document.getElementById('password-error');
        if(password.length < 8 || !password.match(/[A-Z]/) || !password.match(/[a-z]/) || !password.match(/[0-9]/) || !password.match(/[^A-Za-z0-9]/)){
          passwordError.innerHTML = "Password must be at least 8 characters long and contain at least one uppercase letter, one lowercase letter, one digit, and one special character.";
          return false;
        } 
        else{
             passwordError.innerHTML = "";
             return true;
           }
        
          }
    
    function cpass() {
        if (document.getElementById('password').value != document.getElementById('confirm_password').value) {
            var cpasswordError = document.getElementById('cpassword-error');
            cpasswordError.innerHTML = "Passwords do not match.";
            return false;
        } else {
            var cpasswordError = document.getElementById('cpassword-error');
            cpasswordError.innerHTML = ""; // Clear error message when passwords match
            return true;
        }

    }

